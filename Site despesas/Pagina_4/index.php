        <section id="juros">
            <h2>O que são Juros?</h2>
            <article>
                <p>
                    Juros são a remuneração pelo uso do dinheiro ao longo do tempo, funcionando como o "aluguel" de um valor emprestado ou o rendimento de um investimento. Existem os juros simples, calculados sobre o capital inicial, e os juros compostos, que são “juros sobre juros”. Além disso, podemos classificar os juros pelo seu objetivo ou situação, como juros de mora (por atraso de pagamento), juros rotativos (de cartão de crédito), juros nominais (o valor que aparece no contrato) e juros reais (descontada a inflação).
                </p>
                <p>
                    Quanto maior o tempo e a taxa, maior será o valor final a ser pago ou recebido em um investimento.
                </p>
            </article>
