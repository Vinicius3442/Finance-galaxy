            <article>
                <p>
                    Investimento é a aplicação de recursos financeiros (dinheiro) com a expectativa de obter um retorno futuro, que pode ser na forma de juros, dividendos ou valorização do capital. Essencialmente, é fazer com que o seu dinheiro trabalhe para você, aumentando seu patrimônio e ajudando a alcançar metas financeiras como a aposentadoria ou a compra de bens, protegendo seu dinheiro da inflação ao longo do tempo.
                </p>
                <p>
                    Existem vários tipos de investimentos, como renda fixa, ações, fundos e imóveis, cada um com seus riscos e potenciais ganhos.
